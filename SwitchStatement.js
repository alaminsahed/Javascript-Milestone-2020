var today = "friday";
switch (today) {
  case "saturday":
    console.log("saturday");
    break;
  case "sunday":
    console.log("sunday");
    break;
  case "monday":
    console.log("monday");
    break;
  case "tuesday":
    console.log("tuesday");
    break;
  default:
    console.log("No day");
}
